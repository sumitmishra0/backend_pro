// Definition of coercion 
// Whenever we do any operation, based on the input we can actually convert the input for operation.
// Here convert means we can convert type of input.
// This conversion can be manually done by us or the language (or algorithms) based on certain rules automatically converts the types.
// This process of converting the types is typecasting.
// If you are doing this type casting manually yourself then it is Explicit Type Casting.
// And if the language based on certain rules automatically converts the types it is called as Implicit Type casting.
// This term Type Casting is also referred as Type Conversion. So Explicit Type Conversion and Implicit Type Conversion.
// And This Implicit Type conversion is also known as Coercion.